namespace TurtleEngine.Input;

/// <summary>
///     Defines the values that represent a physical mouse button.
/// </summary>
public enum MouseButtons
{
    /// <summary>
    ///     Defines the left mouse button.
    /// </summary>
    Left,

    /// <summary>
    ///     Defines the middle mouse button.
    /// </summary>
    Middle,

    /// <summary>
    ///     Defines the right mouse button.
    /// </summary>
    Right,

    /// <summary>
    ///     Defines the first extra mouse button.
    /// </summary>
    XButton1,

    /// <summary>
    ///     Defines the second extra mouse button.
    /// </summary>
    XButton2
}